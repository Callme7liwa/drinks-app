import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap text-sm font-bold ring-offset-background transition-all focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0 border-2 border-foreground active:translate-x-[2px] active:translate-y-[2px] active:shadow-none",
  {
    variants: {
      variant: {
        default:
          "bg-primary text-primary-foreground shadow-brutal hover:brightness-110",
        destructive:
          "bg-destructive text-destructive-foreground shadow-brutal hover:brightness-110",
        outline:
          "bg-background text-foreground shadow-brutal hover:bg-muted",
        secondary:
          "bg-secondary text-secondary-foreground shadow-brutal hover:brightness-110",
        accent:
          "bg-accent text-accent-foreground shadow-brutal hover:brightness-110",
        highlight:
          "bg-highlight text-highlight-foreground shadow-brutal hover:brightness-110",
        pop:
          "bg-pop text-pop-foreground shadow-brutal hover:brightness-110",
        ghost: "border-0 shadow-none hover:bg-muted hover:text-foreground",
        link: "border-0 shadow-none text-primary underline-offset-4 hover:underline",
        quiz:
          "bg-card text-foreground shadow-brutal hover:bg-primary hover:text-primary-foreground",
      },
      size: {
        default: "h-12 px-6 py-3 rounded-xl text-base",
        sm: "h-10 px-4 py-2 rounded-lg text-sm",
        lg: "h-14 px-8 py-4 rounded-2xl text-lg",
        xl: "h-16 px-10 py-5 rounded-2xl text-xl",
        icon: "h-12 w-12 rounded-xl",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props} />;
  }
);
Button.displayName = "Button";

export { Button, buttonVariants };
